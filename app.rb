enable :sessions

helpers do
  def current_user
    @current_user || nil
  end

  def current_user?
    @current_user == nil ? false : true
  end

  def user_tournament?
    Tournament.find_by(user_id: @current_user.id) == nil ? false : true
  end
end

# Set current user
before do
  @errors ||= []
  @current_user = User.find_by(id: session[:user_id])
end

# Check that user has access to page
# Need to add verification that user has access to the tournament
before /user/  do
  unless current_user?
    @errors << "Please sign up or log in."
    redirect "/"
  end
end

before "/user/tournament/*" do
  unless user_tournament?
    @errors << "Please log in as the tournament creator"
    redirect "/"
  end
end

# Main page, view current tournaments - links to public tournament view, registration
get '/' do
  @tournaments = Tournament.all
	erb :index
end

get '/signup' do
	erb :signup
end

post '/signup' do
	user = User.new(params)
  if user.save
    session[:user_id] = user.id
    redirect('/user')
  else
    @user = user
    erb :signup
  end
end

get '/login' do
	erb :login
end

post '/login' do
	user = User.find_by(email: params[:email])
  if user && user.authenticate(params[:password])
    session[:user_id] = user.id
    redirect('/user')
  else
    @errors << "Invalid email or password. Try again!"
    erb :login
  end
end

get '/logout' do
  session.clear
  redirect('/')
end

