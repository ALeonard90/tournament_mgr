get "/user/tournament/:tournament_id/pool_players" do
  @players = Player.where(tournament_id: params["tournament_id"])

  @players.each do |player|
    unless player.add_to_pool
      @errors << "No pool available for #{player.name}"
    else
      player.save
    end
  end

  redirect "/user/tournament/#{params["tournament_id"]}/pools"
end

get "/user/tournament/:tournament_id/pools/:pool_id" do
  @tournament = Tournament.find(params["tournament_id"])
  @pool = Pool.find(params["pool_id"])
  @players = @pool.players
  unless @pool.matches
    @matches = []
  else
    @matches = @pool.matches.order(:number)
  end

  @matches_per_round = @players.length / 2
  
  until Math.log2(@matches_per_round) % 1 == 0 || @matches_per_round == 2 do
    @matches_per_round +=1
  end

  @start_matches = @matches_per_round
  @num_win_matches = @matches_per_round

  until @matches_per_round == 1 do
    @matches_per_round /= 2
    @num_win_matches += @matches_per_round
  end

  @winning_round_start = []

  x = @num_win_matches
  y = 2

  until x == 1 do
    @winning_round_start << x
    x -= y
    y = y**2
  end

  @winning_round_start << 1

  @losing_round_start = @winning_round_start


  @winner_matches = @matches.select {|match| match.match_type == "W"}
  @loser_matches = @matches.select {|match| match.match_type == "L"}

  erb :user_tournament_pool
end

get "/user/tournament/:tournament_id/pools/:pool_id/create_bracket" do
  @pool = Pool.find(params["pool_id"])
  Match.destroy_all(pool_id: @pool.id)
  @pool.create_matches

  redirect "/user/tournament/#{params["tournament_id"]}/pools/#{params["pool_id"]}"   
end

patch "/user/tournament/:tournament_id/pools/:pool_id" do
  pool = Pool.update(
    name: params[:name],
    gender: params[:gender],
    rank: params[:rank],
    min_weight: params[:min_weight],
    max_weight: params[:max_weight],
    min_age: params[:min_age],
    max_age: params[:max_age]
    )

  redirect "/user/tournament/#{params["tournament_id"]}/pools/#{params["pool_id"]}"
end

get "/user/tournament/:tournament_id/pools/:pool_id/match_winner/:match_id/:player_id" do
  @pool = Pool.find(params["pool_id"])
  @match = Match.find(params["match_id"])

  @match.set_winner(params["player_id"].to_i)
  @pool.next_match(@match)

  redirect "/user/tournament/#{params["tournament_id"]}/pools/#{params["pool_id"]}"
end

delete "/user/tournament/:tournament_id/pools/:pool_id" do
  Pool.destroy(params["pool_id"])

  redirect "/user/tournament/#{params["tournament_id"]}/pools"
end

